const SPECTRALS = [0x7f7fff, 0xbfbfff, 0xffffff, 0xffffbf, 0xffff7f, 0xffbf7f, 0xff7f7f, 0xbf7f7f];

class Constellation {
    constructor(scene) {
        this.scene = scene;
        this.R = 0.4; // 지구 지름의 1/4
        this.stars = [];
        this.connections = [];
        this.center = new THREE.Vector3();
        this.visible = false;
    }

    setCenter(position) {
        this.center.copy(position);
        // 중심점을 약간 내부로 이동
        const normal = position.clone().normalize();
        this.center.add(normal.multiplyScalar(-0.05));
    }

    createStars() {
        // 기존 별 제거
        this.stars.forEach(star => this.scene.remove(star));
        this.stars = [];

        for (const star of STARS) {
            const size = 0.005 - star[10]/2000;
            const geometry = new THREE.SphereGeometry(size, 16, 16);
            const material = new THREE.MeshBasicMaterial({ 
                color: SPECTRALS[star[11]],
                transparent: true,
                opacity: 0.8
            });
            const sphere = new THREE.Mesh(geometry, material);
            
            // 별의 위치 계산 (시차와 적위를 라디안으로 변환)
            const ra = (star[3] * 15 + star[4] * 15 / 60 + star[5] * 15 / 3600) * Math.PI / 180;
            const dec = (star[7] + star[8] / 60 + star[9] / 3600) * Math.PI / 180 * (star[6] === 0 ? 1 : -1);

            const x = this.R * Math.cos(dec) * Math.cos(ra);
            const y = this.R * Math.sin(dec);
            const z = this.R * Math.cos(dec) * Math.sin(ra);

            sphere.position.set(x, y, z);
            sphere.position.add(this.center);
            this.stars.push(sphere);
            this.scene.add(sphere);
        }
    }

    createConnections() {
        // 연결선을 생성하지 않음
        this.connections = [];
    }

    show() {
        this.createStars();
        this.createConnections();
        this.visible = true;
    }

    hide() {
        this.stars.forEach(star => this.scene.remove(star));
        this.connections.forEach(connection => this.scene.remove(connection));
        this.stars = [];
        this.connections = [];
        this.visible = false;
    }
}

export default Constellation; 